    <p>Japan is a sovereign island nation in East Asia. Located in the Pacific Ocean, it lies off the eastern coast of the Asian mainland and stretches from
	the Sea of Okhotsk in the north to the East China Sea and China in the southwest.</p>
    <p>	The kanji that make up Japan's name mean "sun origin", and it is often called the "Land of the Rising Sun". Japan is a stratovolcanic archipelago
	consisting of about 6,852 islands. The four largest are Honshu, Hokkaido, Kyushu, and Shikoku, which make up about ninety-seven percent of Japan's land
	area and often are referred to as home islands. The country is divided into 47 prefectures in eight regions, with Hokkaido being the northernmost prefecture
	and Okinawa being the southernmost one. The population of 127 million is the world's tenth largest. Japanese people make up 98.5% of Japan's total population.
	About 9.1 million people live in Tokyo,the capital of Japan.</p>
<p>Japan attracted 28.69 million international tourists in 2017. Japan has 21 World Heritage Sites, including Himeji Castle, Historic Monuments of Ancient
Kyoto and Nara. Popular foreigner attractions include Tokyo and Hiroshima, Mount Fuji, ski resorts such as Niseko in Hokkaido, Okinawa, riding the shinkansen and
taking advantage of Japan's hotel and hotspring network.</p>
      
   <p> The Travel and Tourism Competitiveness Report 2017 ranks Japan 4th out of 141 countries overall, which was the best in Asia.
   Japan gained relatively high scores in almost all aspects, especially health and hygiene, safety and security, cultural resources and business travel.</p>
